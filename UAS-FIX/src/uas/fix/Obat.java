        layar2.add(LO);
        LO.setVisible(true);
        
        TableModel tm = tbl_obat.getModel();
        
        String nama = tm.getValueAt(baris, 2).toString();
        String kode_Obat = tm.getValueAt(baris, 1).toString();    
