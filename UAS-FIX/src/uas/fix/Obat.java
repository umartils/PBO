        LO.txtkode.setText(kode_Obat);
        LO.jeniscombo.setSelectedItem(jenis_obat);
        LO.penyakitcombo.setSelectedItem(jenis_penyakit);
        LO.txtstok.setText(stok);
        LO.txtharga.setText(harga);
        
