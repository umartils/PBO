    public Invo(String noIden, String namaObat, String jenis, int jumlahObat) {
        initComponents();
        
       String qpasien = "SELECT * FROM pasien WHERE Noidentitas = '" + noIden + "'";
       try (
        java.sql.Connection configDB = (java.sql.Connection) TesKoneksi.configDB();
        java.sql.Statement s = configDB.createStatement();
        java.sql.ResultSet r = s.executeQuery(qpasien)
        ) {
        while (r.next()) {   
            String pasien = r.getString("nama");
            txtpasien.setText(pasien);
        }
       } catch (SQLException ex) {
            Logger.getLogger(TambahData.class.getName()).log(Level.SEVERE, null, ex);
        }
        
        String obat = "SELECT * FROM obat WHERE nama_obat = '" + namaObat + "'";
       try (
        java.sql.Connection configDB = (java.sql.Connection) TesKoneksi.configDB();
        java.sql.Statement s = configDB.createStatement();
        java.sql.ResultSet r = s.executeQuery(obat)
        ) {
        while (r.next()) {   
            int hargaObat = r.getInt("harga_Obat");
            int total = hargaObat * jumlahObat;
            System.out.print(total);
            String totals = String.valueOf(total);
            txtobat.setText(totals);
        }
       } catch (SQLException ex) {
            Logger.getLogger(TambahData.class.getName()).log(Level.SEVERE, null, ex);
        }
       String penanganan = "SELECT * FROM jenis_penangan WHERE jenis_penangan = '" + jenis + "'";
       try (
        java.sql.Connection configDB = (java.sql.Connection) TesKoneksi.configDB();
        java.sql.Statement s = configDB.createStatement();
        java.sql.ResultSet r = s.executeQuery(penanganan)
        ) {
        while (r.next()) {
            int hargaPenanganan = r.getInt("harga");
            String hargaPenang = String.valueOf(hargaPenanganan);
            txtpenanganan.setText(hargaPenang);
        }
       } catch (SQLException ex) {
            Logger.getLogger(TambahData.class.getName()).log(Level.SEVERE, null, ex);
        }
       int obatField = Integer.parseInt(txtobat.getText());
       int penangField = Integer.parseInt(txtpenanganan.getText());
       int total = obatField + penangField;
       String fieldTot = String.valueOf(total);
       txttotal.setText(fieldTot);
    }

